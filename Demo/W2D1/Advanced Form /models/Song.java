package com.codingdojo.advancedform.models;

public class Song {

	private String name;
	private String singer;
	
	public Song() {}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSinger() {
		return singer;
	}

	public void setSinger(String singer) {
		this.singer = singer;
	}
	
	
}
