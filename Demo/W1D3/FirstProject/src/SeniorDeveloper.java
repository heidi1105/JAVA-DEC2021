
public class SeniorDeveloper extends Developer {
	public SeniorDeveloper() {
		super();
		super.setLevel(1000);
	}
	
	
	public SeniorDeveloper(String name) {
		super(name);
		super.setLevel(1000);
	}
	
	public SeniorDeveloper(String name, String phone) {
		super(name, phone);
		super.setLevel(1000);
	}
	
	

}
